package javastudy.faceobject.day02;
/*
 * 静态代码块
 * public class name{
 *   static{
 *       //静态代码块
 * }
 * }
 *
 * //特点，，当第一次用到本类时，只执行一次
 *静态内容总是在前面
 * 典型用法：
 * 用于给静态成员变量一次赋值
 *  */
public class Demo04Static {
    public static void main(String[] args) {
        Person one = new Person();
        Person two = new Person();



    }
}
